              <h3>JKS Philippines</h3>
            <p>There is only one chief instructor within the JKS and that is our <b>Shuseki Shihan Masao Kagawa</b>. Therefore, affiliated countries have technical directors to maintain standards and guide students. JKS (Philippines) is run by a committee among which are International Representative, <b>Vito Logan, JKS Philippines Technical Director</b> and <b>JKS Philippines President</b>, <b>Roland Koschinger</b>. These permanent members are supported and advised by the rest of the committee.</p>
           
            <p>The JKS(Philippines) feels that only by being open and transparent, while simultaneously actively promoting talented students and instructors, can it develop and grow into a major force within the karate world. Members have a unique opportunity to enter Teikyo University and train at one of the best karate university clubs in the world. Under the daily guidance of Kagawa sensei, we at JKS Philippines feel that the only way forward is to help and encourage our students to enter the course, and thereby promote and improve Philippine karate.</p>
